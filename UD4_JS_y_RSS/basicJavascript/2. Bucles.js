// Para hacer un bucle, podemos usar for

for (var i = 0; i < 10; i++) {
    console.log(i)
}

// Para hacer una condicion, podemos usar if

if (edad >= 18) {
    console.log("Eres mayor de edad")
}


// para hacer un while, podemos usar while

while (edad < 18) {

    console.log("Eres menor de edad")
    edad++
}

